CREATE TABLE IF NOT EXISTS stg_ads_stats (
    advert_id UInt32,
    atbs UInt32,
    canceled UInt32,
    clicks UInt32,
    cpc Float32,
    cr Float32,
    ctr Float32,
    orders UInt32,
    shks UInt32,
    sum Float32,
    sum_price UInt32,
    views UInt32,
    booster_stats_avg_position UInt32 DEFAULT NULL,
    booster_stats_date DateTime('UTC') DEFAULT NULL,
    booster_stats_nm UInt32 DEFAULT NULL,
    days_atbs UInt32,
    days_canceled UInt32,
    days_clicks UInt32,
    days_cpc Float32,
    days_cr Float32,
    days_ctr Float32,
    days_orders UInt32,
    days_date DateTime('UTC'),
    days_shks UInt32,
    days_sum Float32,
    days_sum_price Float32,
    days_views UInt32,
