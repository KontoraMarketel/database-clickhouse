    days_apps_nms_cpc Float32,
    days_apps_nms_cr Float32,
    days_apps_nms_ctr Float32,
    days_apps_nms_name String,
    days_apps_nms_nm_id UInt32,
    days_apps_nms_orders UInt32,
    days_apps_nms_shks UInt32,
